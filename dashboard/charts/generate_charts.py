def cmd_iteration(args):
    conn = get_connection()
    test_run_id, combination_id = resolve_test_run(conn, args.test_run_id)
    print(f"Test run:    {test_run_id}")
    print(f"Combination: {combination_id}")

    obs_df = load_observer_events(conn, combination_id, test_run_id)
    cli_df = load_client_events(conn, combination_id, test_run_id)
    if len(cli_df) > 5000 or (not cli_df.empty
            and (cli_df["ts"].max() - cli_df["ts"].min()).total_seconds() > 120):
        anchor = find_failover_anchor(conn, combination_id, test_run_id)
        if anchor:
            ws, we = anchor - pd.Timedelta(seconds=15), anchor + pd.Timedelta(seconds=30)
            obs_df = load_windowed_observer(conn, combination_id, test_run_id, ws, we)
            cli_df = load_windowed_client(conn, combination_id, test_run_id, ws, we)
    if obs_df.empty:
        print("No observer events found.", file=sys.stderr); sys.exit(1)

    fw = load_failover_window(conn, combination_id, test_run_id)
    downtime_s = float(fw["downtime_ms"]) / 1000.0 if fw and fw.get("downtime_ms") else None
    if downtime_s is None:
        ms0 = extract_milestones(obs_df, cli_df)
        if "first_client_failure" in ms0 and "first_recovery" in ms0:
            downtime_s = (ms0["first_recovery"] - ms0["first_client_failure"]).total_seconds()

    out_dir = Path(args.output_dir)
    title   = f"Failover Timeline — {combination_id}"
    ms      = extract_milestones(obs_df, cli_df)

    print("\n--- Gantt chart ---")
    gantt = build_gantt_chart(obs_df, cli_df, combination_id, downtime_s, title)
    _save_png(gantt, out_dir / "failover_gantt.png")

    print("\n--- Phase breakdown ---")
    phase_fig, phases = build_phase_chart(ms, combination_id,
                                          f"Failover Phase Breakdown — {combination_id}")
    if phase_fig:
        _save_png(phase_fig, out_dir / "failover_phases.png")
    else:
        print("  Not enough milestones for phase chart.")

    print("\n--- Overlapping phases ---")
    ov_phases, ov_markers = extract_overlap_phases(obs_df, cli_df)
    ov_fig = build_overlap_phase_chart(ov_phases, ov_markers, combination_id,
                                        f"Failover Phases (Overlapping) — {combination_id}")
    if ov_fig:
        _save_png(ov_fig, out_dir / "failover_phases_overlap.png")
    else:
        print("  Not enough events for overlapping phase chart.")

    if downtime_s: print(f"\nTotal downtime: {downtime_s:.1f}s")
    conn.close()
    print("\nDone!")


def cmd_combo_report(args):
    combination_id = args.combination_id
    session_dir    = Path(args.session_dir)
    out_path       = Path(args.output)

    try:
        session_date = _parse_session_date(session_dir.name.split("_")[0])
    except ValueError as e:
        print(f"Error: cannot parse date from session-dir '{session_dir.name}': {e}", file=sys.stderr)
        sys.exit(1)

    print(f"Combination:  {combination_id}")
    print(f"Session date: {session_date}")
    print(f"Session dir:  {session_dir}")
    print(f"Output:       {out_path}")

    conn     = get_connection()
    runs_df  = _load_session_runs(conn, combination_id, session_date)
    if runs_df.empty:
        print(f"Error: no test runs found for '{combination_id}' on {session_date}", file=sys.stderr)
        sys.exit(1)
    print(f"Found {len(runs_df)} test run(s)")

    patroni_cfg = _load_patroni_config(combination_id)
    iterations: list  = []
    iter_sections: list = []

    for i, (_, run_row) in enumerate(runs_df.iterrows(), start=1):
        run_id   = run_row["id"]
        scenario = run_row["failover_type"] or "unknown"
        print(f"  [{i}/{len(runs_df)}] {run_id}  ({scenario})")
        obs_df, cli_df, fw = load_run_data(conn, combination_id, run_id)
        downtime_s  = float(fw["downtime_ms"]) / 1000.0 if fw and fw.get("downtime_ms") else None
        total_fails = fw.get("total_failures", "—") if fw else "—"
        it_status   = "SUCCESS" if downtime_s is not None else (
            "SUCCESS" if not cli_df.empty and cli_df["success"].all() else "UNKNOWN"
        )
        leader = _infer_killed_leader(obs_df)
        iterations.append({"num": i, "scenario": scenario, "leader": leader,
                            "downtime":      f"{downtime_s:.1f}" if downtime_s else "—",
                            "failed_queries": int(total_fails) if total_fails != "—" else "—",
                            "status": it_status, "run_id": run_id})
        preloaded = _load_iter_charts(session_dir, i, scenario)
        if preloaded:
            print(f"    loaded {len(preloaded)} chart(s) from iter{i}_{scenario}/")
        else:
            print(f"    no pre-rendered charts for iter{i}_{scenario} — will show placeholders")
        iter_sections.append(_render_iteration_section(
            i, run_row, obs_df, cli_df, fw, it_status, leader, preloaded_charts=preloaded,
        ))

    doc = (
        "<!DOCTYPE html>\n<html lang=\"en\">\n<head>\n"
        "<meta charset=\"UTF-8\">\n<meta name=\"viewport\" content=\"width=device-width,initial-scale=1\">\n"
        f"<title>Failover Report — {_h(combination_id)} — {_h(str(session_date))}</title>\n"
        f"<style>{_CSS}</style>\n</head>\n<body>\n"
        + _render_header(combination_id, session_date, patroni_cfg, runs_df, iterations)
        + _render_summary_table(iterations)
        + "\n<h2>Per-Iteration Detail</h2>\n"
        + "\n".join(iter_sections)
        + _render_comparison_section(iterations)
        + _render_report_footer(combination_id, session_date)
        + "\n</body>\n</html>\n"
    )
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(doc, encoding="utf-8")
    print(f"\nReport saved: {out_path}")
    conn.close()


