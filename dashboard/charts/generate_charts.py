def build_phase_chart(milestones: dict, combination_id: str, title: str, subtitle=""):
    """Single stacked horizontal bar showing sequential failover phases."""
    t0 = milestones.get("first_client_failure")
    if t0 is None: return None, {}

    def _dur(a, b):
        if a in milestones and b in milestones:
            return (milestones[b] - milestones[a]).total_seconds()
        return None

    phases = {}
    # Build adaptive phase chain based on available milestones
    # Order: failure → DCS → PG promotion → Patroni detection → routing → recovery
    available = []
    available.append(("first_client_failure", "Failure"))

    if "consul_leader_deleted" in milestones:
        available.append(("consul_leader_deleted", "DCS Detection"))
    if "pg_promoted" in milestones:
        available.append(("pg_promoted", "PG Promotion"))
    elif "patroni_promoted" in milestones:
        available.append(("patroni_promoted", "Patroni Promotion"))
    if "haproxy_up" in milestones:
        available.append(("haproxy_up", "Routing Detection"))
    if "first_recovery" in milestones:
        available.append(("first_recovery", "Client Recovery"))

    # Build phases from consecutive available milestones
    phase_list = []
    for i in range(len(available) - 1):
        key_a = available[i][0]
        key_b = available[i + 1][0]
        label = available[i + 1][1]
        phase_list.append((label, key_a, key_b))

    for name, a, b in phase_list:
        d = _dur(a, b)
        if d is not None: phases[name] = d
    if not phases: return None, {}

    # Threshold below which a label is placed outside (to the right) of its bar
    _MIN_INSIDE_S = 0.5

    fig = go.Figure()
    cumulative = 0.0
    phase_order = ["DCS Detection", "PG Promotion", "Patroni Promotion", "Routing Detection", "Client Recovery"]
    for name in phase_order:
        dur = phases.get(name)
        if dur is None: continue
        label = f"{dur:.2f}s"
        if dur >= _MIN_INSIDE_S:
            tpos = "inside"
            inside_font  = dict(color="white",  size=13, family=FONT_FAMILY)
            outside_font = dict(color=DARK,      size=13, family=FONT_FAMILY)
        else:
            tpos = "outside"
            inside_font  = dict(color="white",  size=13, family=FONT_FAMILY)
            outside_font = dict(color=DARK,      size=13, family=FONT_FAMILY)
        fig.add_trace(go.Bar(
            x=[dur], y=[combination_id], base=[cumulative], orientation="h",
            marker=dict(color=PHASE_COLORS.get(name, GRAY)), name=name,
            text=label, textposition=tpos,
            insidetextfont=inside_font,
            outsidetextfont=outside_font,
            hovertext=f"{name}: {dur:.2f}s", hoverinfo="text",
        ))
        cumulative += dur

    # Total annotation at the right end of the stacked bar
    fig.add_annotation(
        x=cumulative, y=combination_id,
        text=f"<b>Total: {cumulative:.2f}s</b>",
        xanchor="left", yanchor="middle",
        showarrow=False,
        font=dict(size=13, color=DARK, family=FONT_FAMILY),
        xshift=6,
    )

    title_text = (f"{title}<br><sup><span style='color:#666;font-size:12px'>{subtitle}</span></sup>"
                  if subtitle else title)
    fig.update_layout(
        title=dict(text=title_text, font=dict(size=18, family=FONT_FAMILY, color=DARK)),
        font=dict(family=FONT_FAMILY, size=13, color=DARK),
        plot_bgcolor=WHITE, paper_bgcolor=WHITE,
        width=1200, height=360, margin=dict(l=180, r=140, t=70, b=70),
        barmode="stack",
        xaxis=dict(title="Time (s from first failure)", showgrid=True, gridcolor="#ecf0f1"),
        yaxis=dict(showgrid=False),
        legend=dict(orientation="h", yanchor="bottom", y=1.08, xanchor="right", x=1),
    )
    return fig, phases


def build_overlap_phase_chart(phases: list, markers: dict, combination_id: str, title: str, subtitle=""):
    """Each phase on its own lane; spans are independent (may overlap)."""
    if not phases: return None
    all_ts = [p["start"] for p in phases] + [p["end"] for p in phases]
    if markers.get("first_failure"):    all_ts.append(markers["first_failure"])
    if markers.get("first_recovery"):   all_ts.append(markers["first_recovery"])
    w_start, w_end = min(all_ts), max(all_ts)
    lane_names = [p["name"] for p in phases]
    fig = go.Figure()

    # Grey shaded gap between Routing Detection end and Client Recovery start
    # (connection re-establishment window)
    _routing_end = None
    _client_start = None
    for p in phases:
        if p["name"] == "Routing Detection":
            _routing_end = p["end"]
        if p["name"] == "Client Recovery":
            _client_start = p["start"]
    if _routing_end and _client_start and _client_start > _routing_end:
        gap_x0 = (_routing_end  - w_start).total_seconds()
        gap_x1 = (_client_start - w_start).total_seconds()
        gap_s  = (gap_x1 - gap_x0)
        fig.add_shape(
            type="rect",
            x0=gap_x0, x1=gap_x1,
            y0=-0.5, y1=len(lane_names) - 0.5,
            yref="y",
            fillcolor="rgba(180,180,180,0.18)",
            line=dict(width=0),
            layer="below",
        )
        fig.add_annotation(
            x=(gap_x0 + gap_x1) / 2,
            y=len(lane_names) - 0.5,
            yref="y",
            text=f"conn re-establishment<br>({gap_s:.2f}s)",
            showarrow=False,
            font=dict(size=11, color="#888", family=FONT_FAMILY),
            align="center",
            yanchor="bottom",
            bgcolor="rgba(255,255,255,0.80)",
        )

    for p in phases:
        dur_s  = (p["end"] - p["start"]).total_seconds()
        base_s = (p["start"] - w_start).total_seconds()
        label  = "instant" if p.get("instant") else f"{dur_s:.2f}s"
        hover  = (f"{p['name']}: {p['start'].strftime('%H:%M:%S.%f')[:-3]} → "
                  f"{p['end'].strftime('%H:%M:%S.%f')[:-3]} ({dur_s:.3f}s)")
        fig.add_trace(go.Bar(
            x=[max(dur_s, 0.05)], y=[p["name"]], base=[base_s], orientation="h",
            marker=dict(color=p["color"]), text=label, textposition="outside",
            textfont=dict(size=13, color=DARK, family=FONT_FAMILY),
            showlegend=False, hovertext=hover, hoverinfo="text",
        ))
    if markers.get("first_failure"):
        x = (markers["first_failure"] - w_start).total_seconds()
        fig.add_vline(x=x, line=dict(color=RED, width=2, dash="dash"))
        fig.add_annotation(x=x, y=1.05, yref="paper", text="First client failure",
                           showarrow=False, font=dict(size=11, color=RED, family=FONT_FAMILY))
    if markers.get("first_recovery"):
        x = (markers["first_recovery"] - w_start).total_seconds()
        fig.add_vline(x=x, line=dict(color=GREEN, width=2, dash="dash"))
        fig.add_annotation(x=x, y=1.05, yref="paper", text="Client recovery",
                           showarrow=False, font=dict(size=11, color=GREEN, family=FONT_FAMILY))

    title_text = (f"{title}<br><sup><span style='color:#666;font-size:12px'>{subtitle}</span></sup>"
                  if subtitle else title)
    fig.update_layout(
        title=dict(text=title_text, font=dict(size=18, family=FONT_FAMILY, color=DARK)),
        font=dict(family=FONT_FAMILY, size=13, color=DARK),
        plot_bgcolor=WHITE, paper_bgcolor=WHITE,
        width=1200, height=420, margin=dict(l=220, r=80, t=80, b=80),
        barmode="overlay",
        xaxis=dict(title="Time (s from earliest phase start)", showgrid=True, gridcolor="#ecf0f1"),
        yaxis=dict(categoryorder="array", categoryarray=list(reversed(lane_names)), showgrid=False),
    )
    return fig

