def _render_event_timeline(obs_df: pd.DataFrame, cli_df: pd.DataFrame) -> str:
    if obs_df.empty: return "<p>No observer events for this run.</p>"
    t0 = None
    if not cli_df.empty:
        fail = cli_df[~cli_df["success"]]
        if not fail.empty: t0 = fail["ts"].min()
    if t0 is None: t0 = obs_df["ts"].min()
    SIGNIFICANT = {"role_change", "leader_key_deleted", "leader_key_created", "leader_key_change",
                   "timeline_change", "backend_state_change", "vip_state_change",
                   "pg_promote_requested", "pg_ready_accept_connections"}
    df = obs_df[obs_df["event_type"].isin(SIGNIFICANT)].sort_values("ts").head(120)
    if df.empty: df = obs_df.sort_values("ts").head(120)
    rows = ""
    for _, row in df.iterrows():
        el = (row["ts"] - t0).total_seconds()
        sign = "+" if el >= 0 else ""
        old_v = _h(row.get("old_value") or "")
        new_v = _h(row.get("new_value") or "")
        arrow = f"{old_v} → {new_v}" if old_v else new_v
        rows += (f"<tr><td><code>{_h(f'{sign}{el:.3f}s')}</code></td>"
                 f"<td>{_h(row['ts'].strftime('%H:%M:%S.%f')[:-3])}</td>"
                 f"<td>{_h(row['component'])}</td><td>{_h(row['node'])}</td>"
                 f"<td><code>{_h(row['event_type'])}</code></td><td>{arrow}</td></tr>\n")
    cap = (f" <small style='color:#95a5a6'>(showing {len(df)} of {len(obs_df)})</small>"
           if len(obs_df) > len(df) else "")
