def _find_latest_failover_window(conn, cid: str, trid: str = ""):
    """Return (window_start, window_end, resolved_trid). Fixes the empty-trid bug."""
    resolved = trid or _resolve_latest_trid(conn, cid)
    if not resolved:
        return None, None, resolved
    anchor = find_failover_anchor(conn, cid, resolved)
    if not anchor:
        return None, None, resolved
    return anchor - pd.Timedelta(seconds=15), anchor + pd.Timedelta(seconds=30), resolved

