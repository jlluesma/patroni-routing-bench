# ── Color helpers ─────────────────────────────────────────────────────────────
def get_state_color(state: str) -> str:
    if not state:             return "#e74c3c"
    if state in STATE_COLORS: return STATE_COLORS[state]
    s = state.upper()
    if s.startswith("UP"):    return "#2ecc71"
    if s.startswith("DOWN"):  return "#e74c3c"
    return "#95a5a6"

def get_consul_color(event_type: str, new_value: str) -> str:
    if event_type == "leader_key_deleted": return "#e74c3c"
    if event_type in ("leader_key_created", "leader_key_change") and new_value: return "#2ecc71"
    return get_state_color(new_value or "")

def _haproxy_aggregate_color(server_states: dict) -> str:
    primary = {k: v for k, v in server_states.items() if k[0] == "primary_backend"}
    if not primary: return "#f39c12"
    return "#2ecc71" if any(str(v).upper().startswith("UP") for v in primary.values()) else "#e74c3c"

