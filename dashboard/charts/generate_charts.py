_WATERFALL_SQL = """
WITH base AS (
    SELECT tr.id AS run_id, tr.combination_id,
        MIN(ce.ts) FILTER (WHERE NOT ce.success)                                       AS t_first_fail,
        MIN(ce.ts) FILTER (WHERE ce.success AND ce.ts > (
            SELECT MAX(ts) FROM client_events WHERE test_run_id = tr.id AND NOT success
        ))                                                                             AS t_client_recover
    FROM test_runs tr JOIN client_events ce ON ce.test_run_id = tr.id
    WHERE tr.combination_id = %s
    GROUP BY tr.id, tr.combination_id
    HAVING MIN(ce.ts) FILTER (WHERE NOT ce.success) IS NOT NULL
),
milestones AS (
    SELECT b.run_id, b.t_first_fail, b.t_client_recover,
        MIN(oe.ts) FILTER (WHERE oe.component='postgres'
            AND oe.event_type IN ('pg_promote_requested', 'pg_promote_detected'))       AS t_pg_promote,
        MIN(oe.ts) FILTER (WHERE oe.component='patroni'
            AND oe.event_type='role_change' AND oe.new_value='primary'
            AND oe.ts > b.t_first_fail)                                                AS t_patroni_primary,
        MIN(oe.ts) FILTER (WHERE oe.component='consul'
            AND oe.event_type='leader_key_created' AND oe.ts > b.t_first_fail)        AS t_consul_key,
        MIN(oe.ts) FILTER (WHERE oe.component='haproxy'
            AND oe.event_type='backend_state_change' AND oe.new_value='UP'
            AND (oe.detail IS NULL OR oe.detail LIKE '%%primary_backend%%')
            AND oe.ts > b.t_first_fail)                                                AS t_haproxy_up,
        MIN(oe.ts) FILTER (WHERE oe.component='postgres'
            AND oe.event_type='pg_ready_accept_connections'
            AND oe.ts > b.t_first_fail)                                                AS t_pg_ready
    FROM base b LEFT JOIN observer_events oe
        ON oe.test_run_id = b.run_id AND oe.ts > b.t_first_fail
    GROUP BY b.run_id, b.t_first_fail, b.t_client_recover
)
SELECT run_id,
    EXTRACT(EPOCH FROM (t_pg_promote      - t_first_fail))::float AS dt_t0,
    EXTRACT(EPOCH FROM (t_patroni_primary - t_first_fail))::float AS dt_t1,
    EXTRACT(EPOCH FROM (t_consul_key      - t_first_fail))::float AS dt_t2,
    EXTRACT(EPOCH FROM (t_haproxy_up      - t_first_fail))::float AS dt_t3,
    EXTRACT(EPOCH FROM (t_pg_ready        - t_first_fail))::float AS dt_t4,
    EXTRACT(EPOCH FROM (t_client_recover  - t_first_fail))::float AS dt_tc
FROM milestones ORDER BY run_id
"""

def _compute_waterfall(conn, combination_id: str) -> Optional[dict]:
    try:
        with conn.cursor() as cur:
            cur.execute(_WATERFALL_SQL, [combination_id])
            rows = cur.fetchall()
    except Exception as e:
        print(f"  [WARN] Waterfall query failed for {combination_id}: {e}", file=sys.stderr)
        return None
    if not rows: return None
    buckets: dict = {"T0": [], "T1": [], "T2": [], "T3": [], "T4": [], "Tc": []}
    keys = {"T0": 1, "T1": 2, "T2": 3, "T3": 4, "T4": 5, "Tc": 6}
    for row in rows:
        for phase, idx in keys.items():
            v = row[idx]
            if v is not None and not (isinstance(v, float) and math.isnan(v)):
                buckets[phase].append(float(v))
    return {p: statistics.median(vals) if vals else None for p, vals in buckets.items()}

