def _render_batch_heatmap(df: pd.DataFrame) -> str:
    success = df[df["status"] == "SUCCESS"].copy()
    success["downtime_s"] = _to_numeric(success["downtime_s"])
    pivot = (success.groupby(["combo_dir", "scenario"])["downtime_s"]
             .median().unstack("scenario")
             .reindex(columns=[s for s in SCENARIO_ORDER if s in success["scenario"].unique()]))
    if pivot.empty: return "<p><em>No successful runs to plot.</em></p>"
    pivot = pivot.loc[pivot.mean(axis=1).sort_values().index]

    def _safe(v):
        try:
            f = float(v)
            return "" if math.isnan(f) else f"{f:.1f}"
        except (TypeError, ValueError):
            return ""

    z_vals   = [[float(v) if v is not None else float("nan") for v in row] for row in pivot.values.tolist()]
    text_vals = [[_safe(v) for v in row] for row in pivot.values.tolist()]
