def _load_patroni_config(combination_id: str) -> dict:
    candidates = [
        Path(f"dcs/consul/{combination_id}/config/patroni.yml"),
        Path(__file__).parent.parent.parent / f"dcs/consul/{combination_id}/config/patroni.yml",
    ]
    path = next((p for p in candidates if p.exists()), None)
    if path is None: return {}
    text = path.read_text()
    result: dict = {"patroni_yml": str(path)}
    for key in ("ttl", "loop_wait", "retry_timeout"):
        m = re.search(rf"^\s+{key}:\s*(\d+)", text, re.MULTILINE)
        if m: result[key] = int(m.group(1))
    return result

