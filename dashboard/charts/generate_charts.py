def cmd_legacy(args):
    conn = get_connection()
    print("Connected to TimescaleDB")

    if args.compare:
        print("\n--- Comparison: Downtime ---")
        generate_comparison_downtime(conn)
        print("\n--- Comparison: Phase Breakdown ---")
        generate_comparison_phases(conn)
        conn.close()
        print("\nDone!")
        return

    combination_id = args.combination_id
    test_run_id    = args.test_run_id

    if test_run_id and not combination_id:
        with conn.cursor() as cur:
            cur.execute("SELECT DISTINCT combination_id FROM observer_events WHERE test_run_id = %s",
                        (test_run_id,))
            row = cur.fetchone()
        if not row:
            print(f"Error: test_run_id '{test_run_id}' not found", file=sys.stderr); sys.exit(1)
        combination_id = row[0]

    if not combination_id:
        print("Error: --combination-id or --test-run-id required", file=sys.stderr); sys.exit(1)

    # Bug fix: resolve latest test_run_id when empty
    if not test_run_id:
        test_run_id = _resolve_latest_trid(conn, combination_id)
        if not test_run_id:
            print(f"No test runs found for combination '{combination_id}'.", file=sys.stderr)
            sys.exit(1)

    print(f"Combination: {combination_id}")
    print(f"Test run:    {test_run_id}")

    obs_df, cli_df, fw = load_run_data(conn, combination_id, test_run_id)
    if obs_df.empty:
        print("No observer events found.", file=sys.stderr); sys.exit(1)

    downtime_s = float(fw["downtime_ms"]) / 1000.0 if fw and fw.get("downtime_ms") else None
    ms = extract_milestones(obs_df, cli_df)
    title = f"Failover Timeline — {combination_id}"
    run_label = test_run_id or "latest"

    print("\n--- Gantt chart ---")
    gantt = build_gantt_chart(obs_df, cli_df, combination_id, downtime_s, title)
    _save_legacy(gantt, f"{combination_id}_{run_label}_gantt")

    print("\n--- Phase breakdown ---")
    phase_fig, _ = build_phase_chart(ms, combination_id,
                                      f"Failover Phase Breakdown — {combination_id}")
    if phase_fig:
        _save_legacy(phase_fig, f"{combination_id}_{run_label}_phases")

    print("\n--- Overlapping phases ---")
    ov_phases, ov_markers = extract_overlap_phases(obs_df, cli_df)
    ov_fig = build_overlap_phase_chart(ov_phases, ov_markers, combination_id,
                                        f"Failover Phases (Overlapping) — {combination_id}")
    if ov_fig:
        _save_legacy(ov_fig, f"{combination_id}_{run_label}_phases_overlap")

    conn.close()
    print("\nDone!")

# ── CLI entry point ───────────────────────────────────────────────────────────
def main():
    parser = argparse.ArgumentParser(
        description="patroni-routing-bench chart generation",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog=(
            "Subcommands:\n"
            "  iteration     Generate per-iteration PNGs (Gantt, phases, overlap)\n"
            "  combo-report  Generate self-contained HTML session report\n"
            "  batch-report  Generate cross-combo batch HTML report\n\n"
            "Legacy (no subcommand):\n"
            "  --combination-id ID   Charts for the latest run of a combination\n"
            "  --test-run-id ID      Charts for a specific test run\n"
            "  --compare             Cross-combination comparison charts\n"
        ),
    )
    # Legacy flags (used when no subcommand)
    parser.add_argument("--test-run-id",    default="", help="Specific test run ID")
    parser.add_argument("--combination-id", default="", help="Combination ID (uses latest run)")
    parser.add_argument("--compare", action="store_true", help="Generate comparison charts")

    subs = parser.add_subparsers(dest="subcommand")

    p_iter = subs.add_parser("iteration",
                              help="Generate per-iteration PNGs for one test run")
    p_iter.add_argument("--test-run-id",  required=True, help="Test run ID to visualize")
    p_iter.add_argument("--output-dir",   default="./output",
                        help="Directory for output PNGs (default: ./output)")

    p_combo = subs.add_parser("combo-report",
                               help="Generate self-contained HTML session report")
    p_combo.add_argument("--combination-id", required=True,
                         help="Combination identifier (e.g. 06-haproxy-rest-polling)")
    p_combo.add_argument("--session-dir",    required=True,
                         help="Session directory (e.g. /results/06-haproxy.../20260421_214658)")
    p_combo.add_argument("--output",         required=True, help="Output HTML file path")

    p_batch = subs.add_parser("batch-report",
                               help="Generate cross-combo batch HTML report")
    p_batch.add_argument("--batch-csv",  required=True, help="Path to results.csv from run_batch.sh")
    p_batch.add_argument("--batch-dir",  required=True, help="Batch results directory (for context)")
    p_batch.add_argument("--output",     required=True, help="Output HTML file path")

    args = parser.parse_args()

    if args.subcommand == "iteration":
        cmd_iteration(args)
    elif args.subcommand == "combo-report":
        cmd_combo_report(args)
    elif args.subcommand == "batch-report":
        cmd_batch_report(args)
    else:
        if not args.test_run_id and not args.combination_id and not args.compare:
            parser.print_help()
            sys.exit(1)
        cmd_legacy(args)


if __name__ == "__main__":
    main()
